    /**
     * Seed translations for a given language code using the provided key-value map.
     * Returns [created, updated] counts.
     */
    private function seedLanguage(string $code, string $name, string $nativeName, array $translations, bool $isDefault = false): array
    {
        // Create or update language
        Language::firstOrCreate(
            ['code' => $code],
            [
                'name' => $name,
                'native_name' => $nativeName,
                'is_default' => $isDefault,
                'is_active' => true,
            ]
        );

        $inserted = 0;
        $updated = 0;

        foreach ($translations as $key => $value) {
            $translation = Translation::updateOrCreate(
                [
                    'language_code' => $code,
                    'group' => 'frontend',
                    'key' => $key,
                ],
                ['value' => $value]
            );

            if ($translation->wasRecentlyCreated) {
                $inserted++;
            } elseif ($translation->wasChanged()) {
                $updated++;
            }
        }

        return [$inserted, $updated];
    }

    public function run(): void
    {
        $this->command->info('🌐 Seeding translations...');

        // ── 1. English ──
        $this->command->info('   📝 English (en)');
        [$enInserted, $enUpdated] = $this->seedLanguage(
            'en', 'English', 'English',
            self::DEFAULT_TRANSLATIONS,
            isDefault: true
        );
        $this->command->info("      ✓ {$enInserted} created, {$enUpdated} updated");

        // ── 2. French ──
        $this->command->info('   📝 Français (fr)');
        [$frInserted, $frUpdated] = $this->seedLanguage(
            'fr', 'French', 'Français',
            self::FRENCH_TRANSLATIONS
        );
        $this->command->info("      ✓ {$frInserted} created, {$frUpdated} updated");

        // ── 3. Hindi ──
        $this->command->info('   📝 हिन्दी (hi)');
        [$hiInserted, $hiUpdated] = $this->seedLanguage(
            'hi', 'Hindi', 'हिन्दी',
            self::HINDI_TRANSLATIONS
        );
        $this->command->info("      ✓ {$hiInserted} created, {$hiUpdated} updated");

        // ── 4. Clear all translation caches ──
        Cache::forget('translations_en_frontend');
        Cache::forget('translations_fr_frontend');
        Cache::forget('translations_hi_frontend');
        Cache::forget('languages_active');
        Cache::forget('language_default');

        $this->command->info('   ✓ Translation caches cleared');

        // ── 5. Verify by reading back ──
        $enCount = Translation::where('language_code', 'en')
            ->where('group', 'frontend')
            ->count();
        $frCount = Translation::where('language_code', 'fr')
            ->where('group', 'frontend')
            ->count();
        $hiCount = Translation::where('language_code', 'hi')
            ->where('group', 'frontend')
            ->count();

        $this->command->info("   📊 Frontend translations: {$enCount} (en), {$frCount} (fr), {$hiCount} (hi)");
    }
